def make_table(doc, headers, rows, col_widths_cm):
    table = doc.add_table(rows=1+len(rows), cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.autofit = True
    # Headers
    for j, h in enumerate(headers):
        cell = table.cell(0, j)
        set_cell_text(cell, h, bold=True, size=Pt(9.5), color=CLR['primary'])
        set_cell_shading(cell, CLR['table_hdr'])
    # Rows
    for i, row_data in enumerate(rows):
        for j, val in enumerate(row_data):
            cell = table.cell(i+1, j)
            align = WD_ALIGN_PARAGRAPH.LEFT if j == 0 else WD_ALIGN_PARAGRAPH.CENTER
            set_cell_text(cell, val, size=Pt(9), align=align)
            if i % 2 == 1:
                set_cell_shading(cell, 'F8FAFC')
    # Column widths
    for i, w in enumerate(col_widths_cm):
        for row in table.rows:
            row.cells[i].width = Cm(w)
    # Enable text wrap
    tbl = table._tbl
    tblPr = tbl.tblPr if tbl.tblPr is not None else parse_xml(f'<w:tblPr {nsdecls("w")}/>')
    for tr in tbl.iterchildren(qn('w:tr')):
        for tc in tr.iterchildren(qn('w:tc')):
            tcPr = tc.get_or_add_tcPr()
            if tcPr.find(qn('w:noWrap')) is None:
                noWrap = parse_xml(f'<w:noWrap {nsdecls("w")} w:val="0"/>')
                tcPr.append(noWrap)
    return table

